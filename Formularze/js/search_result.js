$(document).ready(function(){
	$('.result').each(function(){
		$(this).find('.result_1').hover(function(){
			$(this).closest('.result').find('.result_2').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_3').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_4').attr('style','text-decoration:underline');
		},function(){
			$(this).closest('.result').find('.result_2').removeAttr('style');
			$(this).closest('.result').find('.result_3').removeAttr('style');
			$(this).closest('.result').find('.result_4').removeAttr('style');
		});
		$(this).find('.result_2').hover(function(){
			$(this).closest('.result').find('.result_1').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_3').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_4').attr('style','text-decoration:underline');
		},function(){
			$(this).closest('.result').find('.result_1').removeAttr('style');
			$(this).closest('.result').find('.result_3').removeAttr('style');
			$(this).closest('.result').find('.result_4').removeAttr('style');
		});
		$(this).find('.result_3').hover(function(){
			$(this).closest('.result').find('.result_2').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_1').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_4').attr('style','text-decoration:underline');
		},function(){
			$(this).closest('.result').find('.result_2').removeAttr('style');
			$(this).closest('.result').find('.result_1').removeAttr('style');
			$(this).closest('.result').find('.result_4').removeAttr('style');
		});
		$(this).find('.result_4').hover(function(){
			$(this).closest('.result').find('.result_2').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_3').attr('style','text-decoration:underline');
			$(this).closest('.result').find('.result_1').attr('style','text-decoration:underline');
		},function(){
			$(this).closest('.result').find('.result_2').removeAttr('style');
			$(this).closest('.result').find('.result_3').removeAttr('style');
			$(this).closest('.result').find('.result_1').removeAttr('style');
		});
	});
});
